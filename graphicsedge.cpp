#include "graphicsedge.h"
#include "graphicsnode.h"

GraphicsEdge::GraphicsEdge(GraphicsNode *sourceNode, GraphicsNode *destNode)
    : arrowSize(10)
{
    setAcceptedMouseButtons(0);
    source = sourceNode;
    dest = destNode;
    source->addEdge(this);
    dest->addEdge(this);
    setZValue(-1);
    adjust();
}
//! [0]

//! [1]
GraphicsNode *GraphicsEdge::sourceNode() const
{
    return source;
}

GraphicsNode *GraphicsEdge::destNode() const
{
    return dest;
}
//! [1]

//! [2]
void GraphicsEdge::adjust()
{
    if (!source || !dest)
        return;
    int startDivisor=2;
    QLineF line(mapFromItem(source,source->nodeWidth/startDivisor,source->nodeHight/startDivisor), mapFromItem(dest, dest->nodeWidth/startDivisor,dest->nodeHight/startDivisor));
    qreal length = line.length();

    prepareGeometryChange();

    if (length > qreal(20.)) {
        QPointF edgeOffset((line.dx() * 10) / length, (line.dy() * 10) / length);
        sourcePoint = line.p1() + edgeOffset;
        destPoint = line.p2() - edgeOffset;
    } else {
        sourcePoint = destPoint = line.p1();
    }
}
//! [2]

//! [3]
QRectF GraphicsEdge::boundingRect() const
{
    if (!source || !dest)
        return QRectF();

    qreal penWidth = 1;
    qreal extra = (penWidth + arrowSize) / 2.0;

    return QRectF(sourcePoint, QSizeF(destPoint.x() - sourcePoint.x(),
                                      destPoint.y() - sourcePoint.y()))
        .normalized()
        .adjusted(-extra, -extra, extra, extra);
}
